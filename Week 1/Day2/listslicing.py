
# List slicing
Friends=["Sushil","Suman","Saini","Prabesh",13]
# Lists element from index m to index n 
print(Friends[0:4]) 
# Lists element from index m to the last element
print(Friends[1:])
# Lists element from the first element to the index n position
print(Friends[:5])
# Lists every 2nd element from index m to the end of the list
print(Friends[0::2])
#Lists element from the back of the list but with not change in the order
print(Friends[-4:])
